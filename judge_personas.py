def get_persona_names() -> list:
    return list(PERSONAS.keys())


def get_persona(name: str) -> dict:
    return PERSONAS[name]


def build_persona_prompt_suffix(persona_name: str) -> str:
    """產生 persona 專屬的 prompt 附加段落,加在 SYSTEM_PROMPT 最後。"""
    p = PERSONAS[persona_name]
    suffix = f"""

# ════════════════════════════════════════
# 你的評審身分:{p['icon']} {p['title']}
# ════════════════════════════════════════

**背景**:{p['background']}

**評審風格**:
{p['personality']}

**重要提醒**:
- 你必須以這個 persona 的角度撰寫整份評審報告
- 在「評審總評」段落,**開頭就要表明你的評審身分與風格**
- 在「給參賽者的綜合提醒」段落,要展現你這位委員的個性語氣
- 評分要符合你的 persona 評分區間(嚴格派偏緊、鼓勵派偏寬等),但**仍須對應投件實際品質**
"""
    return suffix


def get_default_panel(category: str, n: int = 3) -> list:
    """為指定類組推薦預設的評審委員組合(n 位)。"""
    if category == "創新徵文類":
        # 徵文類強調奇想,放入創意派必須
        order = ["創意派", "平衡派", "嚴格派", "鼓勵派", "商業派"]
    elif category == "新創企劃類":
        # 新創強調商業可行性
        order = ["商業派", "平衡派", "嚴格派", "創意派", "鼓勵派"]
    elif category == "社務創新類":
        # 社務強調創益與啟發
        order = ["鼓勵派", "平衡派", "嚴格派", "創意派", "商業派"]
    else:
        # 研發製造、服務行銷:技術與商業並重
        order = ["嚴格派", "商業派", "平衡派", "創意派", "鼓勵派"]

    return order[:n]
