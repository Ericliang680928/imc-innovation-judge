def build_user_prompt(category: str, stage: str, content: str, case_title: str = "") -> str:
    """產生送給 Claude 的 user message。"""
    info = CATEGORY_INFO[category]
    stage_max = info["stage_max"][info["stages"].index(stage)]

    title_line = f"【案件名稱/題目】{case_title}\n" if case_title else ""

    prompt = f"""請以 IMC 第 21 屆創新獎評審委員身份,正式評審以下投件。

【參賽類組】{category}
【評審階段】{stage}
【本階段滿分】{stage_max} 分
{title_line}
【投件全文】
─────────────────────────────────
{content}
─────────────────────────────────

請依照系統指示的評審辦法與評分表,完整輸出八大段落的評審報告。
務必嚴格遵守:
- 每項評分要給具體分數
- 亮點至少 5 項、改善建議至少 5 項、評審提問至少 10 個
- 總評至少 300 字
- 全文繁體中文
"""
    return prompt


def get_categories() -> list:
    """回傳所有可選類組。"""
    return list(CATEGORY_INFO.keys())


def get_stages(category: str) -> list:
    """回傳指定類組的可選階段。"""
    return CATEGORY_INFO[category]["stages"]


def get_stage_max(category: str, stage: str) -> int:
    """回傳指定階段的滿分。"""
    info = CATEGORY_INFO[category]
    return info["stage_max"][info["stages"].index(stage)]
